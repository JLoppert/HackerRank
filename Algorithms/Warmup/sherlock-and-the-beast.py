# Solve: N = (3*x) + (5*y)
# N = length of string
# x = number of 5s
# y = number of 3s
# x = (N - (5*y)) / 3

cases = int(input());
for i in range(cases):
    n = int(input());
    ans = -1;
    for i in range(n):
        x = n - (5*i);
        y = n - x;

        if x < 0 or y < 0:
            break;

        if x % 3 == 0 and y % 5 == 0:
            ans = ('5' * x) + ('3' * y);
            break;

    print(ans);

